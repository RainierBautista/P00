
public class Calculator {
	public int add(int a, int b) {
	return a + b;
	}//end of add
	
	public int substract(int a, int b) {
	return a - b;  
	}//end of subtract
	
	public int multiple(int a, int b) {
		return a * b;
	}//end of multiple
	
	public int divide(int a, int b) {
		return a / b;
	}//end of divide
	
}//end of class